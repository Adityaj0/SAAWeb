document.addEventListener('DOMContentLoaded', function() {
    const themeToggle = document.querySelector('.theme-toggle');
    const body = document.body;

    themeToggle.addEventListener('click', () => {
        body.classList.toggle('dark-theme');
        themeToggle.innerHTML = body.classList.contains('dark-theme') ? '<i class="fas fa-sun"></i>' : '<i class="fas fa-moon"></i>';
    });

    // Smooth scrolling for navigation links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();

            document.querySelector(this.getAttribute('href')).scrollIntoView({
                behavior: 'smooth'
            });
        });
    });

    // Load more art items
    const loadMoreBtn = document.getElementById('load-more');
    const galleryGrid = document.querySelector('.gallery-grid');
    const artItems = [
        'https://via.placeholder.com/300x200?text=Art+5',
        'https://via.placeholder.com/300x200?text=Art+6',
        'https://via.placeholder.com/300x200?text=Art+7',
        'https://via.placeholder.com/300x200?text=Art+8'
    ];

    loadMoreBtn.addEventListener('click', () => {
        artItems.forEach(item => {
            const div = document.createElement('div');
            div.className = 'art-item';
            div.style.backgroundImage = `url('${item}')`;
            galleryGrid.appendChild(div);
        });
        loadMoreBtn.style.display = 'none';
    });

    // Art item click event (placeholder for lightbox or detail view)
    document.querySelectorAll('.art-item').forEach(item => {
        item.addEventListener('click', () => {
            alert('Clicked on artwork! You can implement a lightbox or detail view here.');
        });
    });
});